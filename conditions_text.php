     <ul>
        <li>
          <h5>Article 1. Durée du séjour.</h5>
          <p>Le client signataire du présent contrat conclu pour une durée déterminée ne pourra en aucune circonstance se prévaloir d’un quelconque droit au maintien dans les lieux.</p>
        </li>

        <li>
          <h5>Article 2. Conclusion du contrat.</h5>
          <p>La réservation devient effective dés lors que le client aura fait parvenir au propriétaire un acompte de 30 % du montant total du prix du séjour avec un minimum d’une nuitée, ainsi qu’un exemplaire du contrat signé, ceci avant la date indiquée au recto du contrat. Le solde sera versé à l’arrivée.</p>
        </li>

        <li>
          <h5>Article 3. Annulation par le client</h5>
          <p>Toute annulation doit être notifiée par lettre ou télécopie adressée au propriétaire. Les délais considérés sont comptés à partir de la prise de connaissance par le propriétaire.</p>   
        
          <ol>
            <li>Annulation plus de 24 heures avant le début du séjour : l’acompte reste acquis au propriétaire.<br>
            Annulation moins de 24 heures avant le début du séjour : l’acompte reste acquis au propriétaire qui demandera en outre le paiement du solde du séjour.</li>
            <li>Si le client ne se manifeste pas avant 19 heures le jour prévu de début du séjour, le présent contrat devient nul et le propriétaire peut disposer de la chambre. L’acompte reste acquis au propriétaire qui demandera en outre le paiement du solde su séjour.</li>
            <li>En cas de séjour écourté, le prix correspondant au séjour initial reste intégralement acquis au propriétaire.<br>
            Les prestations supplémentaires non consommées seront remboursées.</li>
          </ol>
        </li>

        <li>
          <h5>Article 4. Annulation par le propriétaire.</h5>
          <p>Lorsque avant le début du séjour le propriétaire annule le séjour, il doit informer le client apr lettre recommandée avec avis de réception.<br>
          Le client, sans préjuger des recours en réparation des dommages éventuellement subie, sera remboursé immédiatement des sommes versées. Il recevra en outre une indemnité égale à la pénalité qu’il aurait supportée si l’annulation était intervenue de son fait à cette date.</p>
        </li>

        <li>
          <h5>Article 5. Arrivée</h5>
          <p>Le client doit se présenter le jour précisé et aux heures indiquées sur le contrat de location. Il doit prévenir le propriétaire en cas d’arrivée différée ou tardive.</p>
        </li>

        <li>
          <h5>Article 6. Règlement du solde</h5>
          <p>Le solde du coût du séjour est à régler à l’arrivée chez le propriétaire.</p>
          <p>Les consommations et prestations supplémentaires non mentionnées dans le contrat de location seront à régler au propriétaire à la fin du séjour.</p>
          <p>Il n’y a pas de taxe de séjour à Sartrouville.</p>
        </li>

        <li>
          <h5>Article 7. Utilisation des lieux</h5>
          <p>Le client devra respecter le caractère paisible des lieux et en faire l’usage conforme à leur destination. Il s’engage à laisser ceux-ci en bon état.</p>
        </li>

        <li>
          <h5>Article 8. Capacité.</h5>
          <p>Le contrat est établi pour une ou deux personnes ; le nombre de clients ne peut en aucun cas être supérieur. Si le nombre de clients dépasse ce nombre contractuel le propriétaire est en mesure de refuser les clients supplémentaires.</p>
          <p>Ce refus ne peut en aucun cas être considéré comme une modification ou une rupture du contrat à l’initiative du propriétaire, de sorte qu’en cas de départ d’un nombre de clients supérieur à ceux refusés, aucun remboursement ne peut être envisagé.</p>
        </li>

        <li>
          <h5>Article 9. Animaux</h5>
          <p>Afin d’assurer un maximum d’hygiène pour les personnes accueillies, aucun animal n’est accepté dans la chambre.</p>
          <p>En cas de non respect de cette clause, le propriétaire est en mesure de refuser l’animal. Ce refus ne peut en aucun cas être considéré comme une modification ou une rupture du contrat à l’initiative du propriétaire, de sorte qu’en cas de départ du client, aucun remboursement ne peut être envisagé.</p>
        </li>

        <li>
          <h5>Article 10. Litiges</h5>
          <p>Il est recommandé de s'adresser à l'Organisme Départemental des Gîtes de France, à l'Office de Tourisme ou au Syndicat d'Initiative local qui interviendra pour favoriser le règlement à l'amiable des litiges.</p>
          <p>Pour tous les litiges qui naîtraient de l'exécution ou de l'interruption du présent contrat et n’auraient pas pu être résolus à l’amiable, seuls les Tribunaux du ressort du lieu de l'immeuble objet de la location sont compétents.</p>
        </li>
      </ul>